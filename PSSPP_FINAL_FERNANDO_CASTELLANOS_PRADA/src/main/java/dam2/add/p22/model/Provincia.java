package dam2.add.p22.model;

public class Provincia {
	
	private String id;
	
	private String nm;

	public Provincia() {

	}

	public Provincia(String id, String nm) {

		this.id = id;
		this.nm = nm;
	}

	public String getId() {
	return id;
	}

	public void setId(String id) {
	this.id = id;
	}

	public String getNm() {
	return nm;
	}

	public void setNm(String nm) {
	this.nm = nm;
	}

}
